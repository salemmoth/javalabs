public class Diagnose {private int mCount;
    private int mCost;

    public Diagnose(int count, int cost) {
        mCount = count;
        mCost = cost;
    }

    public int getCount() {
        return mCount;
    }

    public void setCount(int count) {
        mCount = count;
    }

    public int getCost() {
        return mCost;
    }

    public void setCost(int cost) {
        mCost = cost;
    }
}
